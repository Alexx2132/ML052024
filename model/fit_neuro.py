import numpy as np
from neuron import SingleNeuron

# Пример данных (X - входные данные: [диаметр ствола, диаметр листьев, высота], y - целевые значения)
X = np.array([
    [0.4, 0.3, 1.5],  # Низшие
    [0.7, 0.8, 2.0],  # Высшие
    [0.1, 0.1, 0.5],  # Низшие
    [0.5, 0.4, 0.8],  # Низшие
    [0.9, 0.8, 2.2],  # Высшие
    [0.3, 0.2, 0.6],  # Низшие
    [0.8, 0.7, 1.8]   # Высшие
])

y = np.array([0, 1, 0, 0, 1, 0, 1])  # 0 - низшие растения, 1 - высшие растения

# Инициализация и обучение нейрона
neuron = SingleNeuron(input_size=3)
neuron.train(X, y, epochs=5000, learning_rate=0.1)

# Сохранение весов в файл
neuron.save_weights('neuron_weights.txt')
